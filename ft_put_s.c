#include "ft_printf.h"

int	ft_put_s(char *c)
{
	int	i;

	i = 0;
	if (!c)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (c[i])
	{
		ft_put_c(c[i]);
		i++;
	}
	return (i);
}
