#include "ft_printf.h"

int		ft_put_nbr_u(unsigned int c)
{
	int	i;

	i = 0;
	if (c > 9)
		i = i + ft_put_nbr_u(c / 10);
	i = i + ft_put_c(c % 10 + '0');
	return (i);
}